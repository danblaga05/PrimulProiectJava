package com.company.lab3.Calculator;

public class Calculator {

    public int sum(int firstnr, int secondnr) {
        int result = firstnr + secondnr;
        return result;
    }

    public int dif(int firstnr, int secondnr) {
        int result = firstnr - secondnr;
        return result;
    }

    public int multi(int firstnr, int secondnr) {
        int result = firstnr * secondnr;
        return result;
    }

    public int div(int firstnr, int secondnr) {
        int result = firstnr / secondnr;
        return result;
    }
}
